<h1>NDA Review Checklist: Everything to Look For</h1>
<p class="meta">July 2026 · 7 min read</p>





<p>Non-disclosure agreements (NDAs) are among the most frequently reviewed contracts in legal practice. They're often treated as "simple" or "standard" — but the details matter enormously. A poorly drafted NDA can expose your client to significant risk, or fail to protect them when it matters most.</p>

<p>This checklist covers every clause you should examine when reviewing an NDA, with specific red flags to watch for at each step.</p>

<h2>The Complete NDA Review Checklist</h2>

<div class="checklist">
<div class="checklist-item"><span class="check">☐</span><div><strong>Definition of Confidential Information</strong> — Is the definition broad enough to cover all types of information your client might share? Does it include oral disclosures, not just written? Are there reasonable carve-outs (publicly available info, independently developed, rightfully received from third parties)?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Mutual vs. One-Way</strong> — Is the NDA mutual (both parties disclose and receive) or one-way (only one party's information is protected)? If one-way, is that the correct direction for your client?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Obligations of Receiving Party</strong> — Does the receiving party agree to: (a) maintain confidentiality, (b) not disclose to third parties, (c) use only for the stated purpose, (d) protect with at least reasonable care, (e) return or destroy upon request or termination?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Permitted Disclosures</strong> — Are there carve-outs for: (a) employees and agents with a need to know, (b) professional advisors under their own confidentiality obligations, (c) legal/regulatory requirements? Are the carve-outs properly scoped?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Duration/Term</strong> — How long do the confidentiality obligations last? 2-5 years is standard. Indefinite obligations for trade secrets are acceptable. Watch for terms shorter than the useful life of the information.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Indemnification</strong> — Is there an indemnification clause? Is it mutual or one-sided? Is there a cap? An uncapped, one-sided indemnification obligation is a significant risk.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Limitation of Liability</strong> — Are the liability caps equal for both parties? Are there carve-outs that nullify the cap (e.g., for gross negligence, willful misconduct)? Watch for disproportionate caps.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Return or Destruction of Information</strong> — Is there a clear requirement to return or destroy confidential information upon request or termination? Is there a timeline? Are backup copies addressed?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Non-Use / Non-Circumvention</strong> — Does the NDA prevent the receiving party from using the information to circumvent the disclosing party in business dealings? Is there a non-solicitation clause for employees or customers?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Governing Law and Jurisdiction</strong> — Which jurisdiction's laws govern? Is dispute resolution through litigation or arbitration? Is the venue convenient for your client? Are class action waivers included?</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Assignment</strong> — Can either party assign the NDA without consent? If your client is the disclosing party, they should control whether their confidential information can be transferred to a new entity.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Amendment</strong> — Can the NDA be amended unilaterally? All amendments should require mutual written consent.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Termination</strong> — Can the NDA be terminated for convenience? What happens to confidentiality obligations after termination? (They should survive for the stated duration.)</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Residual Knowledge</strong> — Is there a residual knowledge clause allowing the receiving party to use general knowledge retained "in unaided memory"? These clauses significantly weaken protection.</div></div>

<div class="checklist-item"><span class="check">☐</span><div><strong>Injunctive Relief</strong> — Does the NDA acknowledge that monetary damages alone may be inadequate and allow for injunctive relief? This is important for enforcement.</div></div>
</div>

<h2>Common NDA Red Flags</h2>

<h3>One-Sided Indemnification</h3>
<p>The most common NDA red flag: one party must indemnify the other for any breach, but the obligation isn't mutual. If your client is the one giving the indemnification, they bear all the risk with no reciprocal protection.</p>

<h3>Disproportionate Liability Caps</h3>
<p>Watch for liability caps that differ significantly between parties — for example, $100 for one party and $50,000 for the other. This creates a situation where one party has meaningful exposure while the other has almost none.</p>

<h3>Unilateral Assignment Rights</h3>
<p>If one party can assign the NDA without consent, they can transfer your client's confidential information to a new entity — potentially a competitor. Assignment should require mutual consent.</p>

<h3>Overly Broad Non-Compete</h3>
<p>Some NDAs include non-compete restrictions that are unreasonably broad — worldwide, multi-year, covering all competitors. These may be unenforceable but still create chilling effects and litigation risk.</p>

<h3>Missing Termination Clause</h3>
<p>An NDA without a termination-for-convenience clause locks both parties in indefinitely. Make sure there's a way out with reasonable notice.</p>

<h3>Unilateral Amendment</h3>
<p>If one party can amend the NDA terms without the other's consent, the agreement can change at any time. This is a serious risk — all amendments should require mutual written consent.</p>

