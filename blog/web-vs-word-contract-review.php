<h2>Security Considerations</h2>
<p>Both approaches face similar security questions: Where is your contract data processed? Who has access? How is it encrypted?</p>
<p>The difference is deployment model:</p>
<ul>
<li><strong>Word plugins</strong> process contract text through the provider's cloud APIs (the add-in sends the document text to the provider's servers for AI analysis). The document stays on your machine, but its contents transit through the provider's infrastructure.</li>
<li><strong>Web-based tools</strong> receive the uploaded contract, process it server-side, and return results. The contract text is processed and not stored permanently by reputable providers.</li>
</ul>
<p>In practice, both models send contract text to the provider's AI infrastructure. The key question is what happens to that data after analysis: Is it used for training? Is it stored long-term? Are you notified of sub-processors? A reputable provider in either category should answer these questions clearly in their <a href="/legal/privacy.php">privacy policy</a>.</p>

<h2>Making the Decision: A Simple Framework</h2>
<ol>
<li><strong>Assess your input formats.</strong> If 80%+ of contracts arrive as .docx files you'd open in Word anyway, a plugin is worth considering. If they arrive as PDFs, emails, or varied formats, a web-based tool handles them more directly.</li>
<li><strong>Count your devices.</strong> Do you work on one Windows machine, or across a laptop, home computer, and phone? Web-based tools travel with you.</li>
<li><strong>Count your people.</strong> A 5-attorney firm pays 5× the per-seat price for Word plugins. Web-based tools charge by volume, not people.</li>
<li><strong>Try before you buy.</strong> The best way to evaluate is to run a real contract through each approach. <a href="/free-nda-check.php">Paste a contract into ContractPeer's free tool</a> — no account needed — and see the output in seconds.</li>
<li><strong>Revisit in 6 months.</strong> Your workflow changes. The tool that fits today may not fit in six months. Favor approaches that let you switch without data migration or retraining costs.</li>
</ol>

