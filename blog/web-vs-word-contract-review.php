<h2>Frequently Asked Questions</h2>

<h3>Can I use a web-based tool alongside Microsoft Word?</h3>
<p>Yes. Web-based tools are complementary to Word, not replacements. Use the web tool for quick intake reviews and risk identification, then open the contract in Word for edits based on the analysis.</p>

<h3>Do web-based tools support redlining?</h3>
<p>Some do (ContractPeer's Professional and Firm plans include redlined edit suggestions). The difference is that web-based tools show suggested edits in the analysis report rather than inserting them directly into your Word document.</p>

<h3>Is a web-based tool as secure as a Word plugin?</h3>
<p>Security depends on the provider, not the architecture. Both approaches send contract text to cloud APIs for AI analysis. What matters is the provider's data handling — encryption, retention policy, training usage, and compliance certifications.</p>

<h3>What if I need to review a contract on my phone?</h3>
<p>Web-based tools work in any browser. Upload from your phone's file system, cloud storage, or email attachment. Word plugins require a desktop with Word installed.</p>

<h3>Which is better for a solo attorney?</h3>
<p>For most solo attorneys, a web-based tool offers better value: lower cost ($49/mo vs $99+/mo), no installation, works with any contract format, and no per-seat pricing. The exception is if you do all contract work inside Word and need in-document redlining.</p>
