<h1>Master Services Agreement (MSA) Review Checklist</h1>
<p class="meta">July 2026 · 9 min read</p>




<p>A Master Services Agreement (MSA) is the foundation of most business-to-business relationships. It sets the terms for all future work, so getting it wrong means every subsequent project inherits the same problems. This checklist covers every clause you should examine when reviewing an MSA.</p>

<h2>What Is a Master Services Agreement?</h2>
<p>An MSA is a framework contract that establishes the general terms between two parties — typically a client and a service provider. Individual projects are then governed by Statements of Work (SOWs) that reference the MSA. This means the MSA's terms apply to every project, making it critical to get right.</p>

<h2>The Complete MSA Review Checklist</h2>

<h3>1. Scope of Services</h3>
<p>Does the MSA clearly define what services are covered? Are SOWs required for each project? Is there a process for changing scope? Watch for overly broad service descriptions that could be interpreted to include work beyond what was intended.</p>

<h3>2. Payment Terms</h3>
<ul>
<li><strong>Payment timeline:</strong> Net-30 is standard. Net-60 or net-90 puts significant cash flow pressure on the provider. Watch for mismatches between payment terms and service delivery schedules.</li>
<li><strong>Late fees:</strong> Are late payment penalties reasonable? 1-1.5% per month is standard. Higher rates may be unenforceable in some jurisdictions.</li>
<li><strong>Audit rights:</strong> Does the client have the right to audit the provider's records? What's the notice period and scope?</li>
<li><strong>Setoff rights:</strong> Can the client deduct amounts from payments? This can be a significant risk if broadly drafted.</li>
</ul>

<h3>3. Intellectual Property</h3>
<ul>
<li><strong>Work product ownership:</strong> Does work product transfer to the client immediately upon creation (work-for-hire) or upon payment? Payment-contingent transfer is safer for providers.</li>
<li><strong>Pre-existing IP:</strong> Are the provider's pre-existing tools, frameworks, and methodologies clearly carved out? If not, the MSA could be interpreted as transferring ownership of the provider's core IP.</li>
<li><strong>License-back:</strong> If the provider uses client IP to deliver services, is there a license-back for the provider's continued operations?</li>
<li><strong>Open source:</strong> Does the MSA address open source software usage? Some clients prohibit certain licenses (e.g., GPL).</li>
</ul>

<h3>4. Indemnification</h3>
<ul>
<li><strong>Mutual vs. one-sided:</strong> Is indemnification mutual? One-sided indemnification (only one party indemnifies) is a significant risk for the indemnifying party.</li>
<li><strong>Cap:</strong> Is there a cap on indemnification liability? An uncapped indemnity can be catastrophic. The cap should be tied to the contract value or a multiple thereof.</li>
<li><strong>Scope:</strong> Does indemnification cover IP infringement, third-party claims, breach of contract, and negligence? Each scope expansion increases risk.</li>
<li><strong>Defense obligation:</strong> Who controls the defense of claims? Does the indemnifying party have the right to settle without consent?</li>
</ul>

<h3>5. Limitation of Liability</h3>
<ul>
<li><strong>Cap amount:</strong> Is the liability cap equal for both parties? Are the caps proportionate to the deal size?</li>
<li><strong>Carve-outs:</strong> What liabilities survive the cap? Common carve-outs include gross negligence, willful misconduct, IP infringement, and indemnification. Each carve-out increases exposure.</li>
<li><strong>Consequential damages:</strong> Are indirect, incidental, and consequential damages excluded? This is standard but watch for narrow definitions that limit the exclusion.</li>
</ul>

<h3>6. Termination</h3>
<ul>
<li><strong>For convenience:</strong> Can either party terminate without cause? What's the notice period? 30-60 days is standard.</li>
<li><strong>For cause:</strong> What constitutes a material breach? Is there a cure period? 15-30 days is typical.</li>
<li><strong>Termination for insolvency:</strong> Can either party terminate if the other becomes insolvent or files for bankruptcy?</li>
<li><strong>Transition obligations:</strong> Upon termination, what must the provider deliver? Are there transition assistance obligations? What's the duration and pricing?</li>
<li><strong>Survival:</strong> Which clauses survive termination? (Confidentiality, IP, indemnification, limitation of liability should survive.)</li>
</ul>

<h3>7. Confidentiality</h3>
<ul>
<li><strong>Definition:</strong> Is the definition of confidential information broad enough to cover all sensitive information that might be exchanged?</li>
<li><strong>Duration:</strong> How long do obligations survive? 3-5 years is standard. Indefinite for trade secrets is appropriate.</li>
<li><strong>Permitted disclosures:</strong> Are there carve-outs for employees, advisors, and legal requirements?</li>
<li><strong>Return/destruction:</strong> Is there a requirement to return or destroy confidential information upon termination?</li>
</ul>

<h3>8. Non-Compete and Non-Solicitation</h3>
<ul>
<li><strong>Scope:</strong> Is the non-compete reasonable in geography, duration, and scope? Broad non-competes may be unenforceable (especially in California).</li>
<li><strong>Non-solicitation of employees:</strong> Can the parties hire each other's employees? What's the restricted period?</li>
<li><strong>Non-solicitation of clients:</strong> Can the provider work with the client's customers? This can be very restrictive for the provider.</li>
</ul>

<h3>9. Dispute Resolution</h3>
<ul>
<li><strong>Arbitration vs. litigation:</strong> Is dispute resolution through binding arbitration or court litigation? Arbitration is faster but limits appeal rights.</li>
<li><strong>Jurisdiction and venue:</strong> Which courts have jurisdiction? Is the venue convenient for your client?</li>
<li><strong>Governing law:</strong> Which state's laws govern the agreement?</li>
<li><strong>Class action waivers:</strong> If arbitration is required, are class actions waived? This limits collective claims.</li>
<li><strong>Attorney fees:</strong> Does the prevailing party recover fees? This increases litigation risk.</li>
</ul>

<h3>10. Warranties and Representations</h3>
<ul>
<li><strong>Service warranty:</strong> What warranty does the provider give? Standard is "performed in a professional and workmanlike manner."</li>
<li><strong>Compliance warranty:</strong> Does the provider warrant compliance with applicable laws?</li>
<li><strong>IP warranty:</strong> Does the provider warrant that work product doesn't infringe third-party IP?</li>
<li><strong>Disclaimer:</strong> Are implied warranties disclaimed? (Merchantability, fitness for a particular purpose.)</li>
</ul>

<h3>11. Force Majeure</h3>
<ul>
<li><strong>Coverage:</strong> Does the clause cover modern disruptions (pandemics, cyberattacks, supply chain failures)? An exhaustive list may miss unforeseen events.</li>
<li><strong>Notice and mitigation:</strong> Does the affected party have to give notice and mitigate?</li>
<li><strong>Termination right:</strong> Can either party terminate if the force majeure event lasts beyond a certain period?</li>
</ul>

<h3>12. Assignment and Change of Control</h3>
<ul>
<li><strong>Assignment rights:</strong> Can either party assign without consent? Assignment should require mutual consent.</li>
<li><strong>Change of control:</strong> Does a merger or acquisition trigger assignment restrictions? This is often overlooked but critical.</li>
</ul>

<h3>13. Insurance Requirements</h3>
<ul>
<li><strong>Required coverage:</strong> Does the MSA require specific insurance policies? (General liability, professional liability/E&O, cyber liability, workers' comp.)</li>
<li><strong>Minimum limits:</strong> Are the limits reasonable and appropriate for the services?</li>
<li><strong>Additional insured:</strong> Must the provider name the client as an additional insured?</li>
</ul>

<h2>MSA Red Flags to Watch For</h2>
<ul>
<li><strong>Unilateral amendment rights:</strong> One party can change terms without the other's consent.</li>
<li><strong>Uncapped indemnification:</strong> No limit on the indemnifying party's exposure.</li>
<li><strong>Disproportionate liability caps:</strong> One party's cap is significantly lower than the other's.</li>
<li><strong>Worldwide non-competes:</strong> Overly broad restrictions that may be unenforceable.</li>
<li><strong>Missing termination for convenience:</strong> Locks both parties in for the entire term.</li>
<li><strong>Payment terms exceeding 60 days:</strong> Creates cash flow problems for providers.</li>
<li><strong>IP overreach:</strong> Work-for-hire language that captures pre-existing tools and methodologies.</li>
</ul>

