<h1>How to Review a SaaS Agreement: Complete Guide</h1>
<p class="meta">July 2026 · 8 min read</p>

<p>Software-as-a-Service (SaaS) agreements are among the most frequently reviewed contracts in modern legal practice. Nearly every business uses cloud software, and each subscription comes with terms that govern data ownership, service levels, liability, and termination rights. This guide covers everything attorneys need to check when reviewing a SaaS agreement.</p>

<h2>What Makes SaaS Agreements Different</h2>
<p>Unlike traditional software licenses where software is installed on-premise, SaaS agreements govern access to software hosted by the provider. This creates unique issues around data ownership, service availability, data security, and what happens to data when the relationship ends. The customer is effectively renting access to software and storing their data on someone else's servers.</p>

<h2>The 14 Clauses Every SaaS Agreement Must Address</h2>

<h3>1. Service Level Agreement (SLA)</h3>
<p>Does the contract specify uptime guarantees? What's the target (99.9%? 99.99%?)? Are there service credits for downtime? Is maintenance downtime excluded?</p>
<ul>
<li><strong>Uptime commitment:</strong> 99.9% allows ~43 minutes/month of downtime. 99.99% allows only ~4 minutes. Know the difference.</li>
<li><strong>Service credits:</strong> Are they meaningful? A 5% credit for 8 hours of downtime may be far less than the actual business impact.</li>
<li><strong>Exclusions:</strong> Scheduled maintenance, force majeure, and customer-caused issues are typically excluded.</li>
</ul>

<h3>2. Data Ownership and Portability</h3>
<p>Who owns the data the customer inputs into the SaaS platform? Can the customer export all their data? In what format?</p>
<ul>
<li><strong>Ownership:</strong> The customer should retain ownership of all data they input. Watch for language granting the provider rights to customer data.</li>
<li><strong>Export:</strong> Is there a data export obligation? What format (CSV, JSON, XML)? Is it available during and after the term?</li>
<li><strong>Data deletion:</strong> Upon termination, is the provider required to delete customer data? What's the timeline?</li>
</ul>

<h3>3. Data Security and Breach Notification</h3>
<ul>
<li><strong>Security standards:</strong> Does the provider commit to specific standards (SOC 2, ISO 27001, HIPAA)?</li>
<li><strong>Breach notification:</strong> How quickly must the provider notify the customer of a data breach? 72 hours is common. Does the provider cover notification costs?</li>
<li><strong>Audit rights:</strong> Can the customer audit the provider's security? Are SOC 2 reports provided?</li>
</ul>

<h3>4. Data Processing and Privacy</h3>
<ul>
<li><strong>Processing purpose:</strong> The provider should process customer data only to provide the service. Watch for clauses allowing use for the provider's own purposes.</li>
<li><strong>Sub-processors:</strong> Does the provider use sub-processors? Is there a list? Can the customer object?</li>
<li><strong>GDPR/CCPA compliance:</strong> Does the agreement include a Data Processing Addendum (DPA)? Are international data transfers addressed?</li>
</ul>

<h3>5. Intellectual Property</h3>
<ul>
<li><strong>Platform IP:</strong> The provider retains ownership of the software. This is standard.</li>
<li><strong>Customer data:</strong> The customer retains ownership of their data.</li>
<li><strong>Feedback:</strong> Does the provider get rights to customer feedback or suggestions? Watch for broad feedback licenses.</li>
<li><strong>Custom configurations:</strong> If the customer builds custom workflows or integrations, who owns those?</li>
</ul>

<h3>6. Payment and Auto-Renewal</h3>
<ul>
<li><strong>Pricing:</strong> Is pricing fixed for the term? Are there annual increases (CPI, fixed percentage)?</li>
<li><strong>Auto-renewal:</strong> Does the contract auto-renew? What's the notice period to cancel? Some states require clear disclosure of auto-renewal terms.</li>
<li><strong>Price changes:</strong> Can the provider change pricing mid-term? Watch for clauses allowing unilateral price increases.</li>
</ul>

<h3>7. Termination and Data Return</h3>
<ul>
<li><strong>Termination for convenience:</strong> Can the customer terminate without cause? What's the notice period?</li>
<li><strong>Termination for cause:</strong> What constitutes material breach? Is there a cure period?</li>
<li><strong>Data transition:</strong> Upon termination, how long does the customer have to retrieve data? 30-90 days is standard.</li>
<li><strong>Transition assistance:</strong> Does the provider offer transition support? Is it included or extra cost?</li>
</ul>

<h3>8. Limitation of Liability</h3>
<ul>
<li><strong>Cap:</strong> Is the cap tied to fees paid in the preceding 12 months? This is standard but may be inadequate for data breach exposure.</li>
<li><strong>Carve-outs:</strong> What survives the cap? Data breach, confidentiality, and IP infringement typically survive.</li>
<li><strong>Data breach cap:</strong> Is there a separate (higher) cap for data breach liability? Many providers resist this.</li>
</ul>

<h3>9. Indemnification</h3>
<ul>
<li><strong>IP infringement:</strong> Does the provider indemnify against IP infringement claims? This is critical for SaaS.</li>
<li><strong>Data breach:</strong> Does the provider indemnify for losses from provider-caused breaches?</li>
<li><strong>Third-party claims:</strong> What's the scope of third-party claim coverage?</li>
</ul>

<h3>10. Confidentiality</h3>
<ul>
<li><strong>Mutual:</strong> Is confidentiality mutual? It should be.</li>
<li><strong>Duration:</strong> 3-5 years post-termination is standard.</li>
<li><strong>Definition:</strong> Is the definition broad enough to cover all sensitive information?</li>
</ul>

<h3>11. Usage Limits and Overages</h3>
<ul>
<li><strong>User limits:</strong> Are there seat limits? What happens if exceeded?</li>
<li><strong>Volume limits:</strong> Are there API call limits, storage limits, or bandwidth limits?</li>
<li><strong>Overage pricing:</strong> How are overages priced? Is it transparent?</li>
</ul>

<h3>12. Compliance and Regulatory</h3>
<ul>
<li><strong>Industry-specific:</strong> If the customer is in a regulated industry (healthcare, finance), does the SaaS meet compliance requirements?</li>
<li><strong>Audit:</strong> Does the provider undergo regular compliance audits? Are results available?</li>
<li><strong>Change in compliance:</strong> What happens if the provider loses a compliance certification?</li>
</ul>

<h3>13. Service Changes and Discontinuation</h3>
<ul>
<li><strong>Feature changes:</strong> Can the provider change features? What notice is required?</li>
<li><strong>Discontinuation:</strong> Can the provider discontinue the service entirely? What notice and transition is provided?</li>
<li><strong>Material changes:</strong> Can the customer terminate if material changes are made?</li>
</ul>

<h3>14. Governing Law and Dispute Resolution</h3>
<ul>
<li><strong>Governing law:</strong> Which jurisdiction governs?</li>
<li><strong>Arbitration:</strong> Is arbitration required? What are the rules and venue?</li>
<li><strong>Class action waivers:</strong> Are class actions waived?</li>
</ul>

<h2>SaaS Agreement Red Flags</h2>
<ul>
<li><strong>Unilateral price increases:</strong> Provider can raise prices mid-term.</li>
<li><strong>No data export:</strong> No obligation to return customer data in usable format.</li>
<li><strong>Broad data usage rights:</strong> Provider can use customer data for its own purposes.</li>
<li><strong>No SLA:</strong> No uptime commitment or remedies for downtime.</li>
<li><strong>Automatic renewal without notice:</strong> May be unenforceable in some states.</li>
<li><strong>Low liability cap with no breach carve-out:</strong> Inadequate protection for data incidents.</li>
<li><strong>Feature removal rights:</strong> Provider can remove features without notice or remedy.</li>
</ul>

